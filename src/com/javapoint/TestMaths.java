package com.javapoint;

public class TestMaths {
    public static void main(String[] args) {
        double x = 29;
        double y = 5;
        System.out.println("Maximum number "+ Math.max(x,y));
        System.out.println("Squareroot of y "+ Math.sqrt(y));
        System.out.println("x raised to y "+ Math.pow(x, y));
        System.out.println("Log(x) " + Math.log(x));
        System.out.println("log(y) "+ Math.log(y));
        System.out.println("log base 10 of y   "+ Math.log10(y));
        System.out.println("end");

    }
}